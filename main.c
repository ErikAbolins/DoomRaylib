void UpdateEnemy(Enemy *enemy) {
    if (!enemy->alive) return;
    float dt = GetFrameTime();
    enemy->position.x += enemy->velocity.x * dt;
    enemy->position.y += enemy->velocity.y * dt;

    if (enemy->position.x + enemy->size.x < 0) {
        enemy->alive = false;
    }

}

